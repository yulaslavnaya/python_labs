# В одну строку вводятся фразы, разделенные символом “;”.
# Используя генератор списка, создайте список lengths, содержащий количество слов в каждой фразе из исходного списка.
# Выведите исходный список и список lengths.

phrases = input().split(';')

# Генератор списка для подсчёта слов в каждой фразе
lengths = [len(p.split()) for p in phrases]

print("Исходный список фраз:", phrases)
print("Количество слов в каждой фразе:", lengths)